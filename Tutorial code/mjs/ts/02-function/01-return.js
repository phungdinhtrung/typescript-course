// No return
function funcNoReturn() {
    console.log("funcNoReturn");
}
funcNoReturn();
// Return number
function funcReturnNumber() {
    return 2;
}
console.log(funcReturnNumber());
// Return string
function funcReturnString() {
    return "ABC";
}
console.log(funcReturnString());
// Return Array
function funcReturnArray() {
    return [1, 2, 3];
}
console.log(funcReturnArray());
// Return Object
function funcReturnObject() {
    return { age: 1, name: "ABC" };
}
console.log(funcReturnObject());
// Return Any
function funcReturnAny() {
    return [4, 6, 3];
}
console.log(funcReturnAny());
